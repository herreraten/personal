public class App {
    public static void main(String[] args) throws Exception {

        for (int i = 5; i != 0; i--) {
            System.out.println("CHRISTENNE");
            
        }
        
        int nicknameCount = 5;

        while (nicknameCount != 0) {
            System.out.println("TENTEN");
            nicknameCount--;
        }
        
         int nameCount = 5;

        do {
            System.out.println("HERRERA");
            nameCount--;
        } while (nameCount != 0);
        
    }
}
